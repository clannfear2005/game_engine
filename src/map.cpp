Map::Map()
{
    dirt = TextureManager::LoadTexture("../Textures/dirt.png");
    grass = TextureManager::LoadTexture("../Textures/grass.png");

    LoadMap(map_lvl1);

    rectSrc = {0,0,32,32};
    rectDest = {0,0,32,32};
}

Map::~Map()
{}

void Map::LoadMap( int arr[25][32] )
{
    for (int row = 0; row < 25; row++ )
    {
        for ( int col = 0; col < 32; col++ )
        {
            map[row][col] = arr[row][col];
        }
    }
}

void Map::DrawMap()
{
    int type = 0;

    for (int row = 0; row < 25; row++ )
    {
        for ( int col = 0; col < 32; col++ )
        {
            type = map[row][col];

            rectDest.x = col * 32;
            rectDest.y = row * 32;

            switch (type)
            {
                case 0:
                    TextureManager::Draw(grass, rectSrc, rectDest);
                    break;
                case 1:
                    TextureManager::Draw(dirt, rectSrc, rectDest);
                    break;
                default:
                    break;
            }
        }
    }
}
